#DATATYPE:
#A data type tells Python what kind of value a variable is storing.
#t defines how data is stored in memory and what operations can be done with it.

#EXAMPLE:
a = 10          # int
b = 3.14        # float
c = 2 + 3j      # complex
name = "BK"     # string
status = True   # boolean
nums = [1, 2, 3]  # list
tup = (4, 5, 6)   # tuple
s = {7, 8, 9}     # set
d = {"id": 101, "name": "Kannan"}  # dict
print(type(a), a)
print(type(b), b)
print(type(c), c)
print(type(name), name)
print(type(status), status)
print(type(nums), nums)
print(type(tup), tup)
print(type(s), s)
print(type(d), d)
